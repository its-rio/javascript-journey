//  Array

var arr = [20,45,5,"Rio",false,565,66,66,44]
console.log(arr);             // Print whole array
console.log(arr[1]);         // Print specific digit

console.log(arr.length);      // Length of array

arr.push(22)                 // Add at Last
console.log(arr)

arr.pop()                    // Delete from last
console.log(arr);

arr.shift()                  // Delete from start
console.log(arr);

arr.unshift(123)              // Add at start
console.log(arr);